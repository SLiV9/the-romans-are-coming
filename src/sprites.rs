pub fn draw_surface(x: i32, y: i32, alt: u8)
{
	let frame = (alt as u32) % SURFACE_FRAMES;
	blit_sub(
		&SURFACE_SHEET,
		x + 1 - (SURFACE_WIDTH as i32) / 2,
		y + 1 - (SURFACE_HEIGHT as i32),
		SURFACE_WIDTH,
		SURFACE_HEIGHT,
		frame * SURFACE_WIDTH,
		0,
		SURFACE_SHEET_WIDTH,
		SURFACE_SHEET_FLAGS,
	);
}

