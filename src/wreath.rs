pub fn draw_laurel_wreath(x: i32, y: i32)
{
	blit_sub(
		&SCRIBBLES_PIXELS_LAUREL_WREATH,
		x - (SCRIBBLES_PIXELS_LAUREL_WREATH_WIDTH as i32) / 2,
		y - (SCRIBBLES_PIXELS_LAUREL_WREATH_HEIGHT as i32) / 2,
		SCRIBBLES_PIXELS_LAUREL_WREATH_WIDTH,
		SCRIBBLES_PIXELS_LAUREL_WREATH_HEIGHT,
		0,
		0,
		SCRIBBLES_PIXELS_LAUREL_WREATH_WIDTH,
		SCRIBBLES_PIXELS_LAUREL_WREATH_FLAGS,
	);
}

